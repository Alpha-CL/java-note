package src.old.class_case.class_polymorphism;

public class Father {

    public void eat() {
        System.out.println("[ Father.eat: 不挑食 ]");
    }

    public void sleep() {
        System.out.println("[ Father.sleep: 在哪都能睡 ]");
    }

    public void work() {
        System.out.println("[ Father.work: 工作赚钱 ]");
    }
}
